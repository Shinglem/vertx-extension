package io.github.shinglem.util.id;

public class IdException extends Exception{

    public IdException() {
        super();
    }

    public IdException(String message) {
        super(message);
    }

    public IdException(String message, Throwable cause) {
        super(message, cause);
    }

    public IdException(Throwable cause) {
        super(cause);
    }

}
